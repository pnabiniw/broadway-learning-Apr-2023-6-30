a = ()  # This creates an empty tuple
a = (1, "a", "Broadway", True)  # This is a non-empty tuple. Supports mixed type
a = (1, 2, 3)

a = (1) # This doesn't give tuple rather it gives a integer. To create a tuple we need to add a comma
a = (1, )  # This also creates a tuple
a = ("broadway", )


# Creating a tuple using tuple() function
a = tuple([1, 2, 3])  # It type-casts the list to a tuple

